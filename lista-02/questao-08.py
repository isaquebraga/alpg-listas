"""
    Área de um Retângulo: Escreva um algoritmo que solicita ao usuário a largura e o comprimento de um retângulo, depois calcula e exibe a área do retângulo. A fórmula para calcular a área de um retângulo é área = largura x comprimento.
"""

largura = float(input("Digite a largura do retângulo: "))
comprimento = float(input("Digite o comprimento do retângulo: "))

area = largura * comprimento;

print(f"A área do retângulo descrito é {area:.2f}.")